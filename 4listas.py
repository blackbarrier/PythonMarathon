#Listas indexacion

L = [1,2,3,4,5]
print(L)

print("Acceder por indice")
print(L[0])

print("Acceder por rango")
print(L[0:4])

print("Acceder por rango, no accede al ultimo elemnto")
print(L[:3])

print("Acceder por rango hasta un limite final")
print(L[:-3])

print("Acceder por rango desde -4 hasta -2")
print(L[-4:-2])

print("Acceder por saltos")
print(L[::2])

print("Combinaciones")
print(L[-1::-1])
print(L[-2::-1])



